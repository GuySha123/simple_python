def sumTwoNums(n1, n2):
    return(n1 + n2) 

def multTwoNums(n1, n2):
    return(n1 * n2) 

def divTwoNums(n1, n2):
    return(n1 / n2) 

def sumThreeNums(n1, n2, n3):
    return(n1 + n2 + n3)

def multThreeNums(n1, n2, n3):
    return(n1 * n2 *n3)

def biggerNum(n1, n2):
    if(n1 > n2):
        return(f"{n1} is bigger than {n2}")
    elif(n1 < n2):
        return(f"{n2} is bigger than {n1}")
    else:
        return(f"{n1} is equal to {n2}")